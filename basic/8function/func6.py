def add(num1, num2): 
    result = num1 + num2 
    return result

def sub(num1, num2): 
    result = num1 - num2 
    return result

def multi(num1, num2): 
    result = num1 * num2 
    return result

def divide(num1, num2): 
    result = num1 / num2 
    return result

print(add(20, 10)) 
print(sub(20, 10)) 
print(multi(20, 10)) 
print(divide(20, 10))